'1. Roast the bell peppers. Seed the peppers and peel off the charred skin. For instructions on how to roast bell peppers, click here. Chop the roasted skinned pepper flesh.
2. Seed the jalapeno, then mince it. If you have sensitive skin, use gloves for this step. You can add more jalapenos for more heat if you like a spicy matbucha. As written, the recipe has a little kick, but its not overly spicy.
3. In a medium saucepan, combine diced tomatoes, roasted bell peppers, minced jalapeno, garlic, sugar, chili pepper flakes, and salt. Bring to a simmer over medium high heat.
4. Reduce heat to around medium low, till the matbucha is cooking at a medium and constant simmer. Monitor the temperature of the matbucha, stirring every ten minutes to make sure it doesnt start to stick to the bottom of the pan. Let the mixture cook for 65-75 minutes till most of the liquid has reduced and cooked down.
5. When most of the liquid has evaporated and the mixture is quite thick, you are ready to add your oil.
6. In a small bowl, whisk together olive oil and paprika.
7. Pour the seasoned oil into the pan and stir.
8. Let the matbucha cook for about 30 minutes more, stirring every 5 minutes. Towards the end of cooking, taste the mixture and adjust seasoning as desired-- more sugar for sweetness, chili pepper flakes for spice, and salt if needed.
